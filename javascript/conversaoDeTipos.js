// Conversão de tipos/Concatenação:

console.log('ano' + 2023); // concatena.
console.log('2' + '2'); // concatena.
console.log(parseInt('2') + parseInt('2')); // converte em número inteiro.
console.log('10' / '2'); // aqui faz a operação.
console.log('Ricardo' / '2'); // Not a Number.
console.log(6.5); // lê o número.
console.log(6,5); // lê os 2 números separadamente.
